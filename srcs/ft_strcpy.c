#include "../includes/libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t len;

	if (dst == src)
		return (dst);
	len = ft_strlen(src);
	dst = ft_memcpy(dst, src, (len + 1));
	return (dst);
}
