#include "../includes/libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*new;

	len = ft_strlen(s1) + 1;
	if (!(new = (char *)malloc(sizeof(char) * len)))
		return (NULL);
	return (ft_memcpy(new, s1, len));
}
